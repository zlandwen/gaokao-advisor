        name = uni[1]
        if name in existing:
            skipped += 1
            continue
        
        c.execute("""
            INSERT INTO universities 
            (name, name_en, province, city, level, type, rank_soft, has_qiangji, has_zongping_gd, strong_disciplines, description)
            VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
        """, (
            name,
            uni[2],   # name_en
            uni[3],   # province
            uni[4],   # city
            uni[5],   # level
            uni[6],   # type
            uni[7],   # rank_soft
            uni[8],   # has_qiangji
            uni[9],   # has_zongping_gd
            uni[10],  # strong_disciplines
            uni[11]   # description
        ))
        added += 1
        print(f"  + {name}")
    
